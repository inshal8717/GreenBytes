function transliterateLatin(value) {
  const pairs = [
    ["sh", "ش"], ["ch", "تش"], ["th", "ث"], ["kh", "خ"], ["gh", "غ"], ["ph", "ف"], ["oo", "و"], ["ee", "ي"],
  ];
  let result = value;
  pairs.forEach(([from, to]) => { result = result.replace(new RegExp(from, "gi"), to); });
  const letters = { a: "ا", b: "ب", c: "ك", d: "د", e: "ي", f: "ف", g: "ج", h: "ه", i: "ي", j: "ج", k: "ك", l: "ل", m: "م", n: "ن", o: "و", p: "ب", q: "ق", r: "ر", s: "س", t: "ت", u: "و", v: "ف", w: "و", x: "كس", y: "ي", z: "ز" };
  return result.replace(/[A-Za-z]/g, (letter) => letters[letter.toLowerCase()] || "");
}

export function translateArabicText(value, transliterateUnknown = true) {
  let translated = String(value ?? "");
  if (arabicUi.has(translated.trim())) {
    return translated.replace(translated.trim(), arabicUi.get(translated.trim()));
  }
  [...arabicUi.entries()]
    .sort((a, b) => b[0].length - a[0].length)
    .forEach(([english, arabic]) => { translated = translated.replaceAll(english, arabic); });
  [...arabicFragments.entries()]
    .sort((a, b) => b[0].length - a[0].length)
    .forEach(([english, arabic]) => { translated = translated.replaceAll(english, arabic); });
  return transliterateUnknown && /[A-Za-z]/.test(translated) ? transliterateLatin(translated) : translated;
}

export function applyArabicUi(root) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    if (node.parentElement?.closest("[data-no-translate]")) return;
    const original = node.nodeValue;
    const trimmed = original.trim();
    if (trimmed) node.nodeValue = translateArabicText(original);
  });

  root.querySelectorAll("[placeholder], [aria-label], [title]").forEach((element) => {
    if (element.closest("[data-no-translate]")) return;
    ["placeholder", "aria-label", "title"].forEach((attribute) => {
      const value = element.getAttribute(attribute);
      if (value) element.setAttribute(attribute, translateArabicText(value));
    });
  });

  root.querySelectorAll("input[value], textarea").forEach((element) => {
    const current = element.value;
    const translated = translateArabicText(current, false);
    if (translated !== current) element.value = translated;
  });
}
